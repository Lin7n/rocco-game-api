function list({ page = 1, pageSize = 20, category } = {}) {
  let result = GUIDES;
  if (category) result = result.filter((g) => g.category === category);
  const total = result.length;
  const start = (page - 1) * pageSize;
  return { items: result.slice(start, start + pageSize), total, page, pageSize };
}

function getById(id) {
  return GUIDES.find((g) => g.id === id);
}

function getCategories() {
  return [...new Set(GUIDES.map((g) => g.category))];
}


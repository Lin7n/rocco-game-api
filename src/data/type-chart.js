/**
 * 计算进攻方属性对防御方属性的伤害倍率
 * @param {string|string[]} attackTypes - 攻击方类型(1-2个)
 * @param {string|string[]} defenseTypes - 防御方类型(1-2个)
 * @returns {number} 倍率(0, 0.25, 0.5, 1, 2, 4)
 */
function calculateMultiplier(attackTypes, defenseTypes) {
  const atkArr = Array.isArray(attackTypes) ? attackTypes : [attackTypes];
  const defArr = Array.isArray(defenseTypes) ? defenseTypes : [defenseTypes];
  let multiplier = 1;
  for (const atk of atkArr) {
    if (!ATTACK_MULTIPLIER[atk]) continue;
    for (const def of defArr) {
      const m = ATTACK_MULTIPLIER[atk][def] ?? 1;
      multiplier *= m;
    }
  }
  return multiplier;
}

/**
 * 计算该属性被所有其他属性攻击时的倍率分布
 * 用于阵容覆盖度分析
 */
function getDefensiveMatchups(defenseTypes) {
  return TYPES.map((atkType) => ({
    attacker: atkType,
    multiplier: calculateMultiplier(atkType, defenseTypes),
  }));
}

/**
 * 找出对一个属性组合造成最高倍率的攻击属性
 * 用于PVE克制推荐
 */
function findBestCounter(defenseTypes) {
  const counters = TYPES.map((atkType) => ({
    type: atkType,
    multiplier: calculateMultiplier(atkType, defenseTypes),
  }))
    .filter((c) => c.multiplier >= 2)
    .sort((a, b) => b.multiplier - a.multiplier);
  return counters;
}

